microIoT.microIoT_initDisplay()
basic.showIcon(IconNames.Giraffe)
microIoT.microIoT_setIndexColor(PIN.P1, 0, 15, 0xff0000)
basic.pause(1000)
microIoT.microIoT_setIndexColor(PIN.P1, 0, 15, 0x00ff00)
basic.pause(1000)
microIoT.microIoT_setIndexColor(PIN.P1, 0, 15, 0x0000ff)
basic.pause(1000)
basic.forever(function () {
    microIoT.microIoT_setIndexColor(PIN.P1, 0, 15, microIoT.microIoT_rgb(randint(0, 255), randint(0, 255), randint(0, 255)))
    basic.pause(500)
})
